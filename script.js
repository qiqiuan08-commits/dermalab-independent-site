const languageToggle = document.querySelector(".lang-toggle");
const menuToggle = document.querySelector(".menu-toggle");
const concernButtons = document.querySelectorAll("[data-concern]");
const wechatDialog = document.querySelector(".wechat-dialog");
const formStatus = document.querySelector(".form-status");
let currentLanguage = "zh";
let currentConcern = "barrier";
let cmsContent = null;

function applyDiagnostic() {
  const result = diagnosticResults[currentLanguage][currentConcern];
  document.querySelector("#result-title").textContent = result.title;
  document.querySelector("#result-text").textContent = result.text;
  document.querySelector("#result-duration").textContent = result.duration;
  document.querySelector("#result-products").textContent = result.products;
}

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[language][key] || element.textContent;
  });
  applyCmsContent();
  applyDiagnostic();
}

function applyCmsContent() {
  if (!cmsContent) return;
  document.querySelectorAll(".brand span:last-child, .site-footer span:first-child").forEach((item) => {
    item.textContent = cmsContent.brandName || "DermaLab Institute";
  });
  const hero = cmsContent.hero?.[currentLanguage];
  if (hero) {
    document.querySelector('[data-i18n="heroEyebrow"]').textContent = hero.eyebrow;
    document.querySelector('[data-i18n="heroTitle"]').textContent = hero.title;
    document.querySelector('[data-i18n="heroText"]').textContent = hero.text;
    document.querySelector('[data-i18n="heroPrimary"]').textContent = hero.primary || translations[currentLanguage].heroPrimary;
    document.querySelector('[data-i18n="heroSecondary"]').textContent = hero.secondary || translations[currentLanguage].heroSecondary;
  }
  if (cmsContent.wechat) {
    document.querySelector('[data-i18n="wechatTitle"]').textContent = cmsContent.wechat.title;
    document.querySelector('[data-i18n="wechatText"]').textContent = cmsContent.wechat.note;
    document.querySelector(".qr-placeholder span").textContent = cmsContent.wechat.qrText || "QR";
  }
  if (Array.isArray(cmsContent.resources)) {
    document.querySelectorAll("[data-download-demo]").forEach((link, index) => {
      const resource = cmsContent.resources[index];
      if (!resource) return;
      link.textContent = resource.title;
      link.setAttribute("href", resource.url || "#");
    });
  }
}

async function loadCmsContent() {
  if (location.protocol === "file:") return;
  try {
    const response = await fetch("/api/site-content", { cache: "no-store" });
    if (!response.ok) return;
    cmsContent = await response.json();
    applyCmsContent();
  } catch {
    cmsContent = null;
  }
}

languageToggle.addEventListener("click", () => {
  applyLanguage(currentLanguage === "zh" ? "en" : "zh");
});

menuToggle.addEventListener("click", () => {
  document.querySelector(".site-header").classList.toggle("nav-open");
});

document.querySelectorAll(".nav a").forEach((link) => {
  link.addEventListener("click", () => {
    document.querySelector(".site-header").classList.remove("nav-open");
  });
});

concernButtons.forEach((button) => {
  button.addEventListener("click", () => {
    currentConcern = button.dataset.concern;
    concernButtons.forEach((item) => item.classList.toggle("active", item === button));
    applyDiagnostic();
  });
});

document.querySelector(".contact-form").addEventListener("submit", (event) => {
  event.preventDefault();
  const form = event.currentTarget;
  if (!form.checkValidity()) {
    formStatus.textContent = translations[currentLanguage].formMissing;
    form.reportValidity();
    return;
  }
  const button = event.currentTarget.querySelector("button");
  const originalText = button.textContent;
  button.textContent = currentLanguage === "zh" ? "已收到预约" : "Booking received";
  formStatus.textContent = translations[currentLanguage].formSuccess;
  if (location.protocol !== "file:") {
    const payload = Object.fromEntries(new FormData(form).entries());
    payload.language = currentLanguage;
    fetch("/api/leads", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload)
    }).catch(() => {});
  }
  window.setTimeout(() => {
    button.textContent = originalText;
  }, 1800);
});

document.querySelectorAll(".js-open-wechat").forEach((button) => {
  button.addEventListener("click", () => {
    if (typeof wechatDialog.showModal === "function") {
      wechatDialog.showModal();
    }
  });
});

document.querySelector(".dialog-close").addEventListener("click", () => {
  wechatDialog.close();
});

document.querySelectorAll("[data-download-demo], .js-open-report").forEach((item) => {
  item.addEventListener("click", (event) => {
    if (item.matches("[data-download-demo]") && item.getAttribute("href") && item.getAttribute("href") !== "#") {
      return;
    }
    event.preventDefault();
    window.alert(translations[currentLanguage].downloadNotice);
  });
});

applyLanguage(currentLanguage);
loadCmsContent();
